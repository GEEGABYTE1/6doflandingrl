"""Generate reproducible Phase 1 plots from saved LQR trajectory outputs."""

from __future__ import annotations

import argparse
import csv
import os
from pathlib import Path

import numpy as np

os.environ.setdefault("MPLCONFIGDIR", str(Path("outputs/.cache/matplotlib").resolve()))
os.environ.setdefault("XDG_CACHE_HOME", str(Path("outputs/.cache").resolve()))

import matplotlib.pyplot as plt


def parse_args() -> argparse.Namespace:
    """Parse command-line arguments."""
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument(
        "--input-csv",
        type=Path,
        default=Path("outputs/phase1_lqr/trajectory.csv"),
        help="Trajectory CSV generated by simulate_lqr.py.",
    )
    parser.add_argument(
        "--output-dir",
        type=Path,
        default=Path("outputs/phase1_lqr/figures"),
        help="Directory where plots are saved.",
    )
    return parser.parse_args()


def load_trajectory(path: Path) -> dict[str, np.ndarray]:
    """Load a trajectory CSV into named NumPy arrays."""
    with path.open("r", newline="", encoding="utf-8") as stream:
        rows = list(csv.DictReader(stream))
    if not rows:
        raise ValueError(f"No trajectory rows found in {path}.")
    return {
        key: np.array([float(row[key]) for row in rows], dtype=float)
        for key in rows[0].keys()
    }


def save_plots(data: dict[str, np.ndarray], output_dir: Path) -> list[Path]:
    """Save trajectory, state-history, and control-history plots."""
    output_dir.mkdir(parents=True, exist_ok=True)
    saved: list[Path] = []
    time = data["time_s"]

    fig = plt.figure(figsize=(7.0, 5.5))
    ax = fig.add_subplot(111, projection="3d")
    ax.plot(data["x_m"], data["y_m"], data["z_m"])
    ax.set_xlabel("x inertial [m]")
    ax.set_ylabel("y inertial [m]")
    ax.set_zlabel("altitude z [m]")
    ax.set_title("Phase 1 LQR Baseline Landing Trajectory")
    path = output_dir / "trajectory_3d.png"
    fig.tight_layout()
    fig.savefig(path, dpi=180)
    plt.close(fig)
    saved.append(path)

    fig, axes = plt.subplots(3, 1, figsize=(8.0, 7.0), sharex=True)
    axes[0].plot(time, data["z_m"])
    axes[0].set_ylabel("altitude [m]")
    axes[1].plot(time, data["vx_mps"], label="vx")
    axes[1].plot(time, data["vy_mps"], label="vy")
    axes[1].plot(time, data["vz_mps"], label="vz")
    axes[1].set_ylabel("velocity [m/s]")
    axes[1].legend(loc="best")
    axes[2].plot(time, data["mass_kg"])
    axes[2].set_ylabel("mass [kg]")
    axes[2].set_xlabel("time [s]")
    fig.suptitle("Phase 1 LQR State Histories")
    path = output_dir / "state_histories.png"
    fig.tight_layout()
    fig.savefig(path, dpi=180)
    plt.close(fig)
    saved.append(path)

    fig, axes = plt.subplots(3, 1, figsize=(8.0, 7.0), sharex=True)
    axes[0].plot(time, data["throttle"])
    axes[0].set_ylabel("throttle [-]")
    axes[1].plot(time, np.rad2deg(data["gimbal_pitch_rad"]), label="pitch")
    axes[1].plot(time, np.rad2deg(data["gimbal_yaw_rad"]), label="yaw")
    axes[1].set_ylabel("gimbal [deg]")
    axes[1].legend(loc="best")
    axes[2].plot(time, data["dynamic_pressure_pa"])
    axes[2].set_ylabel("q dyn [Pa]")
    axes[2].set_xlabel("time [s]")
    fig.suptitle("Phase 1 LQR Control and Aero Histories")
    path = output_dir / "control_histories.png"
    fig.tight_layout()
    fig.savefig(path, dpi=180)
    plt.close(fig)
    saved.append(path)
    return saved


def main() -> None:
    """CLI entry point."""
    args = parse_args()
    data = load_trajectory(args.input_csv)
    saved = save_plots(data, args.output_dir)
    for path in saved:
        print(path)


if __name__ == "__main__":
    main()
